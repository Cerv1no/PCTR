/**
 * Esta clase contiene los metodos y atributos de critica
 * @author Álvaro Álvarez Cerviño
 * @version 2222.0
 */
public class critica {
    private int n = 0;

    /**
     * Método constructor por defecto
     */
    public critica() {}

    /**
     * Método de incremento de la variable n
     */
    public void inc() {n++;}

    /**
     * Método de decremento de la variable n
     */
    public void dec() {n--;}

    /**
     * Método para devolver la variable n
     * @return devuelve n
     */
    public int vDato() {return n;}
}
